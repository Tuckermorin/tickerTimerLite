// Helper function to get random period for multiple stocks
export const getRandomMultiStockPeriod = (gameMode = 'classic') => {
  let periodLength;
  
  if (gameMode === 'speedrun') {
    periodLength = 120; // 10 years * 12 months
  } else {
    periodLength = 240; // 20 years * 12 months
  }
  
  // Find the maximum start index that works for all stocks
  const maxStartIndex = Math.min(
    ...Object.values(stockHistoricalData).map(data => data.length - periodLength)
  );
  
  const startIndex = Math.floor(Math.random() * maxStartIndex);
  
  // Extract the same time period for all stocks
  const result = {};
  Object.keys(stockHistoricalData).forEach(symbol => {
    const selectedData = stockHistoricalData[symbol].slice(startIndex, startIndex + periodLength);
    result[symbol] = selectedData.reverse(); // Reverse to go chronologically
  });
  
  return result;
};

